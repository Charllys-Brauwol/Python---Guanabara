#Crie um script em py que leia dois números
#e tente mostrar a soma entre eles.

#Recebendo os dois números pelo input
numero01 = input ('Digite o Primeiro número: ')
numero02 = input ('Digite o Segundo número: ')
#Converte para inteiro, soma e guarda na variável "soma"
soma = int (numero01) + int (numero02)

#Printando na tela a Soma
print('A soma de', numero01, '+', numero02, 'é:', soma) 